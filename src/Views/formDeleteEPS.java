    void getEPS(){
        controllerCiclistas ctrl = new controllerCiclistas();
        ctrl.getEps();
        ResultSet data = ctrl.getEps();
        try {
            while (data.next()) {
                cbxEPS.addItem(data.getString("nombre"));
            }
        } catch (Exception e) {
            System.out.println("Error loading eps");
        }
    }
    void delete(){
        controllerCiclistas ctrl = new controllerCiclistas();
        modelEPS model = new modelEPS();
        String name = cbxEPS.getSelectedItem().toString();
        int eps = ctrl.getID(name, "eps");
        int result = JOptionPane.showConfirmDialog(null, "Est√°s seguro de que deseas elmiminar de los registros a "+ name);
        if (result == 0) {
            model.delete(eps);
            this.dispose();
            
        }
    }
    

