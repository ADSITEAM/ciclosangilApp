    void createEPS(){
        String EPS = txtEPS.getText().toUpperCase();
        if (!EPS.equals("")) {
            modelEPS model = new modelEPS();
            boolean state = model.validateEPS(EPS);
            if (state) {
                model.save(EPS);
                this.dispose();
            } else{
                formMessage modal = new formMessage();
                modal.showModal(true, EPS + " ya est√° registrada.");
            }
        }
    }
